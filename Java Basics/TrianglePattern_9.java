package core.java.java.basics;

public class TrianglePattern_9 {
	public static void main(String[] args) {
		{
			int [][] a = new int [5][5];
			for (int i = 0; i < a.length; i++) {
				System.out.println("");
				for (int j = 0; j < i; j++) {
					int s = i;
					System.out.print(s+ " ");
			}
			}
			}
	}
}
